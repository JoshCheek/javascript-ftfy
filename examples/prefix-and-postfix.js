var a = 1, b = 1, c = 1, d = 1
console.log(a)  // => 1
console.log(--a)  // => 0
console.log(a)  // => 0

console.log(b)  // => 1
console.log(b--)  // => 1
console.log(b)  // => 0

console.log(c)  // => 1
console.log(c++)  // => 1
console.log(c)  // => 2

console.log(d)  // => 1
console.log(d++)  // => 1
console.log(d)  // => 2
